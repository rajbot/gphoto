<h2>libgphoto2</h2>
<p class="text">
libgphoto2 is the core of gphoto2 software. It is a portable library
which gives access to literally
<a href="libgphoto2/support.php">hundreds of digital cameras</a>.</p>
<p class="more"><a href="libgphoto2/" title="more about libgphoto2">(more...)</a></p>

<h2>gphoto2</h2>
<p class="text">
gphoto2 is the command line (CLI) interface to libgphoto2. It allows almost 
everything that libgphoto2 can do.</p>
<p class="more"><a href="gphoto2/" title="more about gphoto2">(more...)</a></p>


<h2>DigiKam</h2>
<p class="text">
digiKam is an advanced digital photo management application for Linux,
Windows, and Mac-OSX.

The people who inspired digiKam's design are the photographers like
you who want to view, manage, edit, enhance, organize, tag, and share
photographs under Linux systems.</p>
<p class="text">
It uses libgphoto2 for importing photos and for capture.
</p>
<p class="more"><a href="http://www.digikam.org">DigiKam</a></p>

