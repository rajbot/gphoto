<tr class="text"><td>
<p>
On this page, you find list of the supported cameras as of the current release.
</p>
<p>
<strong>Supported</strong> means here: Able to download images from the camera (or upload images to the picture frame).
For remote control support check out the <a href="/doc/remote/">Remote Control Overview</a> page.
</p>
<p>
Support for additional cameras may be in the current libgphoto2 SVN trunk code
and will be added to the next release. Cameras capable of MTP, PTP or PictBridge are supported without needing
to be specifically listed here.
</p>
<p>If your camera is neither supported in the current release nor in current SVN trunk, it is possible that
<ul>
<li>it is an old camera for which the original <a href="/proj/gphoto/">gPhoto</a> driver has not been ported yet (mostly due to lack of demand)</li>
<li>it is a new camera for which there is no support at all</li>
</ul>
</p>
<p>
To report a not yet supported camera to the gphoto development team follow the instructions below:
<ul>
<li>If it a USB mass storage based camera (appearing like a USB stick or USB drive) it is mounted by your operating system and not gphoto2. In this case a report to us is not necessary.
<li>Record the output of <code>lsusb</code> to get the USB ids.
<li>Record the output of <code>gphoto2 --auto-detect</code> to see if it is detected in a generic way, or by another name. If it is detected already, run the steps below:
<li>Record the output of <code>LANG=C gphoto2 --summary &gt;summary.txt</code> to get generic summary information into the summary.txt file.
<li>Record the output of <code>LANG=C gphoto2 --list-all-config &gt;config.txt</code> to get the configuration tree into the config.txt file.
<li>Record the output of <code>gphoto2 --capture-image</code> to see if capture works already.
</ul>
Mail the output results with the camera name to <code>gphoto-devel@lists.sourceforge.net</code>.
</p>
<p>
Please note that PTP cameras will already work out of the box, including special vendor features if we already implemented them.
</p>
<p>
See the
<a href="http://www.teaser.fr/~hfiguiere/linux/digicam.html">Digital
Camera Support for UN*X</a> page for details about just any camera.
</p>

